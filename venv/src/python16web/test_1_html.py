#   <title>Hello</title>
# </head>
# <body>
#   <h1>Hello, world!</h1>
# </body>
# </html>
#
#
# HTML文档就是一系列的Tag组成，
# 最外层的Tag是<html>。
#
# 规范的HTML也包含<head>...</head>和<body>...</body>（注意不要和HTTP的Header、Body搞混了），
# 由于HTML是富文档模型，所以，还有一系列的Tag用来表示链接、图片、表格、表单等等。

